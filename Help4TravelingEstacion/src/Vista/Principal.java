    public void centrarVentana(JInternalFrame jif) {
        Dimension tamEscritorio = escritorio.getSize();
        Dimension tamVentana = jif.getSize();
        int width = (tamEscritorio.width - tamVentana.width) / 2;
        int height = (tamEscritorio.height - tamVentana.height) / 2;
        jif.setLocation(width, height);
        cerrarMenu.setEnabled(true);
        cerrarMenu2.setEnabled(true);
    }

    public void ejecutarScript(String ruta, Boolean interno) {
        Conexion conexion = Conexion.getInstance();
        Connection con = conexion.getConnection();
        if (!conexion.getEstado()) {
            JOptionPane.showMessageDialog(null, "No se pudo establecer la conexión.", "Aviso", JOptionPane.ERROR_MESSAGE);
            return;
        }
        ScriptRunner runner = new ScriptRunner(con, false, true);
        try {
            this.setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
            if (interno) {
                runner.runScript(new BufferedReader(new InputStreamReader(this.getClass().getResourceAsStream(ruta))));
            } else {
                runner.runScript(new BufferedReader(new FileReader(ruta)));
            }
            this.setCursor(Cursor.getDefaultCursor());
            JOptionPane.showMessageDialog(this, "El script fue ejecutado correctamente.", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            System.out.println("Corriendo Script... OK");
        } catch (IOException ex) {
            System.out.println("Corriendo Script... ERROR");
            JOptionPane.showMessageDialog(this, "El script no pudo cargarse.", "Error", JOptionPane.ERROR_MESSAGE);
            this.setCursor(Cursor.getDefaultCursor());
        } catch (SQLException ex) {
            System.out.println("Corriendo Script... ERROR");
            JOptionPane.showMessageDialog(this, "El script fue ejecutado con errores.", "Error", JOptionPane.ERROR_MESSAGE);
            this.setCursor(Cursor.getDefaultCursor());
        }
    }

