    private static String dateToString(Date date){
        String fecha, dia, mes, ano;
        dia = String.valueOf(date.getDay());
        mes = String.valueOf(date.getMonth());
        ano = String.valueOf(date.getYear()-100);
        
        fecha = "20" + ano;
        fecha += "-";
        if (mes.length() == 1){
            fecha += "0" + mes;
        } else {
            fecha += mes;
        }
        fecha += "-";
        if (dia.length() == 1){
            fecha += "0" + dia;
        } else {
            fecha += dia;
        }
        return fecha;
    }

